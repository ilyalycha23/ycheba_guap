-- ЛР2, п. 2.5: примеры некорректных вставок (нарушение check / unique / FK).
-- Выполнять по одному оператору: каждый должен завершиться ошибкой.

-- 1) Нарушение check: категория должности вне допустимого множества
INSERT INTO должность (ид, наименование, категория) VALUES (100, 'Тест', 'неверно');

-- 2) Нарушение unique: повтор наименования должности
INSERT INTO должность (ид, наименование, категория) VALUES (101, 'Техник', 'прочее');

-- 3) Нарушение FK: несуществующий родитель подразделения
INSERT INTO подразделение (ид, наименование, ид_родительское) VALUES (50, 'Ошибка', 999);

-- 4) Нарушение check: пол ребёнка не «М» и не «Ж»
INSERT INTO дети (ид, ид_сотрудника, фио, пол, дата_рождения) VALUES (200, 1, 'Тест', 'X', '2011-01-01');

-- 5) Нарушение check: подразделение «откуда» = «куда» в переводе
INSERT INTO перевод (ид, ид_сотрудника, ид_подразделения_откуда, ид_подразделения_куда, дата_перевода, ид_новой_должности)
VALUES (90, 3, 2, 2, '2023-01-01', NULL);

-- 6) Нарушение check: подразделение ссылается само на себя как на родителя
INSERT INTO подразделение (ид, наименование, ид_родительское) VALUES (60, 'Сам себе родитель', 60);

-- 7) Нарушение FK: приём на несуществующего сотрудника
INSERT INTO прием (ид, ид_сотрудника, ид_подразделения, ид_должности, дата_приема, совместительство)
VALUES (90, 9999, 1, 1, '2025-01-01', '0'::bit(1));
