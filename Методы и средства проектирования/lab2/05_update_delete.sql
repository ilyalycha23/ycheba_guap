-- Пример delete с where: удаление временно добавленной строки
INSERT INTO дети (ид, ид_сотрудника, фио, пол, дата_рождения)
VALUES (99, 8, 'Временная запись для удаления', 'М', '2015-05-05');

DELETE FROM дети
WHERE ид = 99;
