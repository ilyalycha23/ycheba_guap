-- Пример update с условием where: временно «портим» ФИО и возвращаем обратно
UPDATE сотрудник
SET фио = 'Иванов Иван Иванович (временная правка)'
WHERE ид = 1;

UPDATE сотрудник
SET фио = 'Иванов Иван Иванович'
WHERE ид = 1;

