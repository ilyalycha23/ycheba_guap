-- а) Список сотрудников заданного подразделения.
-- Пример: задано подразделение "Отдел главного конструктора".
SELECT
    с.ид,
    с.фио,
    пд.наименование AS подразделение,
    д.наименование AS должность
FROM сотрудник с, прием пр, подразделение пд, должность д
WHERE с.ид = пр.ид_сотрудника
  AND пр.ид_подразделения = пд.ид
  AND пр.ид_должности = д.ид
  AND пд.наименование = 'Отдел главного конструктора'
ORDER BY с.фио;

-- б) Подразделения, входящие в состав заданного.
-- Пример: задано "ООО «Промтех»".
SELECT
    доч.ид,
    доч.наименование AS подразделение_в_составе,
    род.наименование AS родитель
FROM подразделение доч, подразделение род
WHERE доч.ид_родительское = род.ид
  AND род.наименование = 'ООО «Промтех»'
ORDER BY доч.наименование;

-- в) Сотрудники, у которых есть дети различного пола.
SELECT DISTINCT
    с.ид,
    с.фио
FROM сотрудник с, дети д1, дети д2
WHERE с.ид = д1.ид_сотрудника
  AND с.ид = д2.ид_сотрудника
  AND д1.пол <> д2.пол
ORDER BY с.фио;