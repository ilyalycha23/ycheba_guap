-- 1) distinct + as:
-- список категорий должностей без повторов
SELECT DISTINCT
    д.категория AS категория_должности
FROM должность д
ORDER BY категория_должности;

-- 2) order by + as:
-- сотрудники по дате рождения (сначала самые молодые)
SELECT
    с.фио AS сотрудник,
    с.дата_рождения AS дата_рождения
FROM сотрудник с
ORDER BY с.дата_рождения DESC, с.фио;

-- 3) [not] in:
-- сотрудники, работающие не в подразделениях 2 и 4
SELECT DISTINCT
    с.ид,
    с.фио,
    пр.ид_подразделения AS подразделение
FROM сотрудник с, прием пр
WHERE с.ид = пр.ид_сотрудника
  AND пр.ид_подразделения NOT IN (2, 4)
ORDER BY с.фио;

-- 4) [not] between ... and ...:
-- сотрудники, родившиеся в диапазоне дат
SELECT
    с.ид,
    с.фио,
    с.дата_рождения
FROM сотрудник с
WHERE с.дата_рождения BETWEEN DATE '1980-01-01' AND DATE '1995-12-31'
ORDER BY с.дата_рождения;

-- 5) is null:
-- подразделения верхнего уровня (без родителя)
SELECT
    п.ид,
    п.наименование
FROM подразделение п
WHERE п.ид_родительское IS NULL;

-- 6) is not null:
-- подразделения, у которых задан родитель
SELECT
    п.ид,
    п.наименование,
    п.ид_родительское
FROM подразделение п
WHERE п.ид_родительское IS NOT NULL
ORDER BY п.наименование;

-- 7) [not] like:
-- сотрудники, фамилия которых начинается на "К"
SELECT
    с.ид,
    с.фио
FROM сотрудник с
WHERE с.фио LIKE 'К%'
ORDER BY с.фио;

-- 8) not like:
-- сотрудники, фамилия которых НЕ начинается на "С"
SELECT
    с.ид,
    с.фио
FROM сотрудник с
WHERE с.фио NOT LIKE 'С%'
ORDER BY с.фио;
