-- г) Средняя численность подразделений.
-- Численность подразделения = сумма штатных единиц по строкам штатного_расписания;
-- «средняя численность» = среднее этих сумм по всем подразделениям (AVG по группам).
SELECT
    ROUND(AVG(per_dept.сумма_единиц)::numeric, 4) AS средняя_численность_подразделений
FROM (
    SELECT
        шр.ид_подразделения,
        SUM(шр.количество_единиц) AS сумма_единиц
    FROM штатное_расписание шр
    GROUP BY шр.ид_подразделения
) AS per_dept;

-- д) Инженеры, у которых более пяти детей.
-- Инженер: у сотрудника есть приём на должность с категорией «инженер».
SELECT
    с.ид,
    с.фио,
    COUNT(д.ид) AS число_детей
FROM сотрудник с
    INNER JOIN дети д ON д.ид_сотрудника = с.ид
WHERE EXISTS (
    SELECT 1
    FROM прием пр
        INNER JOIN должность дл ON дл.ид = пр.ид_должности
    WHERE пр.ид_сотрудника = с.ид
      AND дл.категория = 'инженер'
)
GROUP BY с.ид, с.фио
HAVING COUNT(д.ид) > 5
ORDER BY с.фио;

-- е) Подразделения, в которых количество техников превышает количество инженеров.
-- Считаем различных сотрудников по текущим приёмам (таблица прием) и категории должности.
SELECT
    пд.ид,
    пд.наименование AS подразделение,
    COUNT(DISTINCT CASE WHEN дл.категория = 'техник' THEN пр.ид_сотрудника END) AS техников,
    COUNT(DISTINCT CASE WHEN дл.категория = 'инженер' THEN пр.ид_сотрудника END) AS инженеров
FROM подразделение пд
    INNER JOIN прием пр ON пр.ид_подразделения = пд.ид
    INNER JOIN должность дл ON дл.ид = пр.ид_должности
GROUP BY пд.ид, пд.наименование
HAVING COUNT(DISTINCT CASE WHEN дл.категория = 'техник' THEN пр.ид_сотрудника END)
    > COUNT(DISTINCT CASE WHEN дл.категория = 'инженер' THEN пр.ид_сотрудника END)
ORDER BY пд.ид;
