-- 1) MIN / MAX: по подразделению — первая и последняя дата приёма.
SELECT
    пд.ид,
    пд.наименование AS подразделение,
    MIN(пр.дата_приема) AS первый_прием,
    MAX(пр.дата_приема) AS последний_прием
FROM подразделение пд
    INNER JOIN прием пр ON пр.ид_подразделения = пд.ид
GROUP BY пд.ид, пд.наименование
ORDER BY пд.ид;

-- 2) MIN / MAX: по должности — минимальное и максимальное число штатных единиц в одной строке штатного расписания.
SELECT
    д.ид,
    д.наименование AS должность,
    MIN(шр.количество_единиц) AS мин_единиц_в_строке,
    MAX(шр.количество_единиц) AS макс_единиц_в_строке
FROM должность д
    INNER JOIN штатное_расписание шр ON шр.ид_должности = д.ид
GROUP BY д.ид, д.наименование
ORDER BY д.ид;
