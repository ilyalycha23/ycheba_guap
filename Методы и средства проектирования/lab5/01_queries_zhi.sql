-- ж) Подразделения, в которых не работают совместители
--    (нет ни одной строки приёма с совместительство = 1 в этом подразделении).
SELECT
    пд.ид,
    пд.наименование AS подразделение
FROM подразделение пд
WHERE NOT EXISTS (
    SELECT 1
    FROM прием пр
    WHERE пр.ид_подразделения = пд.ид
      AND пр.совместительство = B'1'
)
ORDER BY пд.ид;

-- з) Подразделения, в которых представлены все должности (ид 1–4 в справочнике
--    имеют строку в штатное_расписание для этого подразделения).
--    После 04_extra_data_lr5.sql таким будет подразделение 3 (ОГК).
SELECT
    пд.ид,
    пд.наименование AS подразделение
FROM подразделение пд
WHERE NOT EXISTS (
    SELECT 1
    FROM должность д
    WHERE д.ид IN (1, 2, 3, 4)
      AND NOT EXISTS (
          SELECT 1
          FROM штатное_расписание шр
          WHERE шр.ид_подразделения = пд.ид
            AND шр.ид_должности = д.ид
      )
)
ORDER BY пд.ид;

-- и) Сотрудники, у которых все дети одного пола
--    (есть хотя бы один ребёнок и нет двух детей с разными полами).
SELECT
    с.ид,
    с.фио
FROM сотрудник с
WHERE EXISTS (
    SELECT 1
    FROM дети д
    WHERE д.ид_сотрудника = с.ид
)
  AND NOT EXISTS (
    SELECT 1
    FROM дети д1
        CROSS JOIN дети д2
    WHERE d1.ид_сотрудника = с.ид
      AND d2.ид_сотрудника = с.ид
      AND d1.ид < d2.ид
      AND d1.пол <> d2.пол
)
ORDER BY с.фио;
