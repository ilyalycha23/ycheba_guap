-- ЛР8: логическое представление «приём на работу» с расшифровкой справочников.

CREATE OR REPLACE VIEW lr8_vw_priyom_details AS
SELECT
    пр.ид AS прием_ид,
    с.ид AS сотрудник_ид,
    с.фио AS сотрудник_фио,
    пд.ид AS подразделение_ид,
    пд.наименование AS подразделение,
    дл.ид AS должность_ид,
    дл.наименование AS должность,
    дл.категория AS должность_категория,
    пр.дата_приема,
    пр.совместительство
FROM прием пр
INNER JOIN сотрудник с ON с.ид = пр.ид_сотрудника
INNER JOIN подразделение пд ON пд.ид = пр.ид_подразделения
INNER JOIN должность дл ON дл.ид = пр.ид_должности;

COMMENT ON VIEW lr8_vw_priyom_details IS 'ЛР8: все приёмы с ФИО и названиями подразделения и должности.';

