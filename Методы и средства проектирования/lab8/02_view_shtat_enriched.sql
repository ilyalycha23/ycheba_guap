-- ЛР8: представление «штатное расписание» с читаемыми названиями.

CREATE OR REPLACE VIEW lr8_vw_shtat_enriched AS
SELECT
    ш.ид AS штат_ид,
    ш.ид_подразделения,
    пд.наименование AS подразделение,
    ш.ид_должности,
    дл.наименование AS должность,
    дл.категория AS должность_категория,
    ш.количество_единиц
FROM штатное_расписание ш
INNER JOIN подразделение пд ON пд.ид = ш.ид_подразделения
INNER JOIN должность дл ON дл.ид = ш.ид_должности;

COMMENT ON VIEW lr8_vw_shtat_enriched IS 'ЛР8: строки штатного расписания с подписью подразделения и должности.';

